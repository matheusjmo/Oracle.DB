SELECT E.CPF_ESTUDANTE, E.COD_PLANO, P.MENSALIDADE FROM ESTUDANTE E
INNER JOIN PLANO P
ON E.COD_PLANO = P.PLANO_ID
WHERE P.MENSALIDADE <> 300;

SELECT P.NOME, T.SALARIO, E.CIDADE
FROM PROFESSOR T
INNER JOIN PESSOA P
ON T.CPF_PROFESSOR = P.CPF
INNER JOIN ENDERECO E
ON P.CPF = E.CPF_PESSOA 
ORDER BY T.SALARIO;